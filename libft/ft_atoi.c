#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	is_minus;
	int	i;
	int	res;

	i = 0;
	res = 0;
	is_minus = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			is_minus = 1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + (nptr[i] - '0');
		i++;
	}
	if (is_minus)
		res = res * (-1);
	return (res);
}
